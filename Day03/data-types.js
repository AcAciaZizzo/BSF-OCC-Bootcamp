//primitive data types

//numbers
let score = 58;
console.log("Type of score:", typeof score);
console.log('Score:', score);
//单引号和双引号都一样

//string
let PlayerName = "Emilio";
console.log("Type of PlayerName:", typeof PlayerName);
console.log('PlayerName:', PlayerName);
//数值和字符串的区别是设定时是否加引号

//布尔值 Boolean - Represents a value of true or false
let isAdmin = true;
isAdmin = false;
console.log('The type of isAmin:', typeof isAdmin);

//空值 Null - Represents intentional absence of value
let car = null;
console.log('The value of car is:', car);
console.log('The type of car is:', typeof car);

//Undefined: Represents unintentional absence of value
let city;
console.log('The value of city is:', city);
console.log('The type of city is:', typeof city);
